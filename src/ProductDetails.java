    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String pid,pname,rate,desc,activate,quantity;
        DefaultTableModel model;
        model = new DefaultTableModel();
        try
        {
            DriverManager.registerDriver(new com.mysql.jdbc.Driver());
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3307/billing?zeroDateTimeBehavior=convertToNull","root","");
            PreparedStatement st=con.prepareStatement("Select * from product");
            ResultSet rs=st.executeQuery();
            while(rs.next())
            {
                pid=String.valueOf(rs.getString(1));
                pname=rs.getString(2);
                rate=String.valueOf(rs.getString(3));
                desc=rs.getString(4);
                activate=rs.getString(5);
                quantity=rs.getString(6);
                String tbdata[]={pid,pname,rate,desc,activate,quantity};
                model=(DefaultTableModel)jTable1.getModel();
                model.addRow(tbdata);
            }
        }catch(Exception ex)
        {
            System.out.println("Error : "+ex);
        }
    }//GEN-LAST:event_formComponentShown

